                break
            case "RAWSFCODE_01":
                //执行逻辑节点[附加到判断结果]
                executeRawsfcode01(iDELogicSession, iPSDELogicNode)
                break
            case "RAWSFCODE_02":
                //执行逻辑节点[执行脚本代码]
                executeRawsfcode02(iDELogicSession, iPSDELogicNode)
                break
            case "LOOPSUBCALL_02":
